</p>
<p>
	美好的事物总是让人难以忘怀，美丽的瞬间总是铭刻于心。
</p>
<p>
	美，不一定要长久，却可以在你的心底刻上最深的印痕。美丽，就如初见，永远有一份温暖……
</p>
<p>
	走过山路，淌过小河，旅途中，能有挚友同行，亦是旅程里一道别致的风景。
</p>
<p>
	挚友开玩笑的说，七月，我愿牵着你的手，在万丈红尘中，赴一场风花雪月的盛宴。
