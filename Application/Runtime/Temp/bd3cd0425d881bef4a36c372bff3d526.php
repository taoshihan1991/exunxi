</p>
<p>
	总有一段时光，在我们的历程里，念念不忘；总有一片记忆，在我们心海里，熠熠闪光；总有一些人，在我们生命里，永远温暖，总有一份情，在纯真的记忆里，日久醇香。人生中，会有无数的相遇，也有许多次重逢。相遇了，庆幸没有错过，重逢了，会意的轻轻一笑，依然同行。感恩每一次的驻足，不忘一程山水中风雨兼程；期许陌上花开，珍惜每一次的再次相聚。不管未来比是记得还是忘记我，我都无怨无悔，情愿做生命路途上陪你安静开放那朵兰花。
</p>
<p>
